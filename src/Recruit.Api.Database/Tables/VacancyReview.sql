    [Id]	                                    uniqueidentifier	NOT NULL,
    [VacancyReference]                          BIGINT              NOT NULL,
    [VacancyTitle]                              nvarchar(500)       NOT NULL,
    [CreatedDate]                               datetime            NOT NULL,
    [SlaDeadLine]                               datetime            NOT NULL,
